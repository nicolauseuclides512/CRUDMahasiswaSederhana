    private void ubahBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahBtnActionPerformed
        int selectedRow = mahasiswaTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Tidak ada data yang dipilih");
            return;
        }

        Mahasiswa selectedMahasiswa = mahasiswaList.get(selectedRow);
        MahasiswaFrame activity = new MahasiswaFrame();
        activity.setTitle("Ubah Data Mahasiswa");
        activity.isiText(selectedMahasiswa);
        open(activity);
    }//GEN-LAST:event_ubahBtnActionPerformed

    private void hapusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBtnActionPerformed
        int selectedRow = mahasiswaTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Tidak ada data mahasiswa terpilih dalam tabel");
            return;
        }

        Mahasiswa selectedMahasiswa = mahasiswaList.get(selectedRow);
        int confirm = JOptionPane.showConfirmDialog(this, "Yakin Hapus " + selectedMahasiswa.getNama() + " ?");
        if (confirm == JOptionPane.YES_OPTION) {
            MahasiswaController datasource = new MahasiswaController();
            try {
                datasource.delete(selectedMahasiswa.getNim());
                mahasiswaTableModel.removeRow(selectedRow);
                mahasiswaList.remove(selectedMahasiswa);
                JOptionPane.showMessageDialog(null, "Data mahasiswa berhasil dihapus");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data mahasiswa gagal dihapus");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_hapusBtnActionPerformed

