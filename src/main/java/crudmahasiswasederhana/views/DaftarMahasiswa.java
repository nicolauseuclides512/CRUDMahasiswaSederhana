    private void awalTabelModel() {
        String[] columns = new String[]{"NIM", "Nama", "Alamat"};
        mahasiswaTableModel = new DefaultTableModel(columns, 0);
        mahasiswaTable.setModel(mahasiswaTableModel);
    }

    private void tambahDataTabel(Mahasiswa mhs) {
        mahasiswaTableModel.addRow(new Object[]{
            mhs.getNim(),
            mhs.getNama(),
            mhs.getAlamat()
        });
    }

    private void tambahBarisTabel(List<Mahasiswa> mahasiswaList) {
        for (Mahasiswa mhs : mahasiswaList) {
            tambahDataTabel(mhs);
        }
    }

    private void loadDataMahasiswa() {
        MahasiswaController mhsC = new MahasiswaController();

        if (mahasiswaTableModel.getRowCount() > 0) {
            mahasiswaTableModel.setRowCount(0);
        }

        try {
            mahasiswaList = mhsC.getAll();
            tambahBarisTabel(mahasiswaList);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Tidak dapat memuat data mahasiswa");
            e.printStackTrace();
        }
        jScrollPane1.setViewportView(mahasiswaTable);
    }

    private void open(Window activity) {
        activity.setLocationRelativeTo(this);
        activity.setVisible(true);
        activity.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                super.windowClosed(e); //To change body of generated methods, choose Tools | Templates.
                loadDataMahasiswa();
            }
        });
    }

